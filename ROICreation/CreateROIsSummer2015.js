// Create a Landsat 8, median-pixel composite for summer of 2015.
var vizParams = {bands: ['B5', 'B4', 'B3'], min: 0, max: 0.4};
var summer2015 = ee.ImageCollection('LANDSAT/LC8_L1T_TOA')
	.filterDate('2015-06-01', '2015-08-30');

var median2015 = summer2015.median();

// Get boundary of Central Valley
var centralValley = ee.FeatureCollection('ft:1h46ENpEp8vO3pOe1EqeF1sZLEDhSVMxbu8pHAoU4', 'geometry');

// Clip Landsat composite by California state boundary.
var clipped2015 = median2015.clip(centralValley);

// Display.
Map.addLayer(clipped2015, vizParams);

// Using the display created above, I used the geometry drawing tool to create regions of interest as
// feature collections, which import at the top of the script.
// 0: BuiltUp
// 1: Water
// 2: BareSoil
// 3: Vegetation
var regionsOfInterest = builtUp.merge(water).merge(bareSoil).merge(vegetation);
Map.addLayer(regionsOfInterest);

// Export to Google Drive
Export.table(regionsOfInterest, 'exportRegionsOfInterest', {
	driveFileNamePrefix: 'regionsOfInterestSummer2015',
	fileFormat: 'KML'});

